(
    id            INTEGER,
    name          TEXT,
    description   TEXT,
    base_price    INTEGER,
    health_points INTEGER,
    mana_points  INTEGER,
    strength      INTEGER,
    agility       INTEGER,
    wisdom        INTEGER,
    constitution            INTEGER,
    to_dodge           INTEGER,
    to_hit           INTEGER,
    attack        INTEGER,
    defense       INTEGER,
    magical_strength           INTEGER,
    durability    INTEGER,
    req_strength  INTEGER,
    req_agility        INTEGER,
    req_wisdom    INTEGER
)
